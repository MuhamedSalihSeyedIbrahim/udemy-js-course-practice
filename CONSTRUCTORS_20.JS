// CONSTRUCTOR IS A FUNCTION HAVE BLUEPRINT TO INSTANTIATE A OBJECT USING NEW KEY WORD
function Apple(x,y,color,score){
//NOW THIS REFER AN OBJECT OF APPLE
  this.x=x;
  this.y=y;
  this.color=color;
  this.score=score;
  //BY DEFAULT IT RETURN THE OBJECT SO NO NEED FOR RETURN THIS EXCLICITLY
}
//JUST INSTANTIATE
new Apple(10,20,"red",90);

